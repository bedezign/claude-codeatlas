def _write(path: Path, body: str, written: list[str]) -> None:
    """Write body to path, prepending the banner. Track for stdout output."""
    if not body.endswith("\n"):
        body += "\n"
    path.write_text(BANNER + "\n\n" + body, encoding="utf-8")
    written.append(str(path))


