def _changed_paths_for_base(conn: sqlite3.Connection, base_sha: str) -> list[str]:
    """Return file paths whose stored ``files.sha`` differs from ``base_sha``.

    The comparison is a literal string match — every file whose current sha
    is not exactly ``base_sha`` is treated as "changed". This is the simplest
    interpretation of the spec; a richer per-file lookup against git history
    can replace this helper without touching the rest of the BFS.
    """
    rows = conn.execute(
        "SELECT path FROM files WHERE sha <> ? ORDER BY path",
        (base_sha,),
    ).fetchall()
    return [row[0] for row in rows]


def _outgoing_neighbours(
    conn: sqlite3.Connection, file_paths: set[str]
) -> dict[str, set[str]]:
    """Map each file path to the set of files it has outgoing edges to.

    Builds a file-level adjacency dict by joining symbols → edges → symbols.
    Edge kind is ignored — any kind contributes to the impact BFS.
    """
    if not file_paths:
        return {}
    placeholders = ",".join("?" * len(file_paths))
    rows = conn.execute(
        f"SELECT DISTINCT fs.path, fd.path "
        f"FROM edges e "
        f"JOIN symbols ss ON ss.id = e.src_id "
        f"JOIN files fs ON fs.id = ss.file_id "
        f"JOIN symbols sd ON sd.id = e.dst_id "
        f"JOIN files fd ON fd.id = sd.file_id "
        f"WHERE fs.path IN ({placeholders})",
        tuple(file_paths),
    ).fetchall()
    out: dict[str, set[str]] = {}
    for src, dst in rows:
        out.setdefault(src, set()).add(dst)
    return out


def _bfs_impact(
    conn: sqlite3.Connection, start: str, max_depth: int
) -> dict[int, list[str]]:
    """BFS over outgoing edges from ``start``, grouped by hop distance.

    Returns ``{1: [...direct...], 2: [...transitive...]}``. The starting
    file is not included in any bucket, and a node visited at hop ``k`` is
    not revisited at any later hop (BFS yields shortest-distance first).
    """
    by_depth: dict[int, list[str]] = {}
    visited: set[str] = {start}
    frontier: set[str] = {start}
    for depth in range(1, max_depth + 1):
        adj = _outgoing_neighbours(conn, frontier)
        next_frontier: set[str] = set()
        for src in frontier:
            for dst in adj.get(src, ()):
                if dst in visited:
                    continue
                next_frontier.add(dst)
        if not next_frontier:
            break
        visited.update(next_frontier)
        by_depth[depth] = sorted(next_frontier)
        frontier = next_frontier
    return by_depth


def _render_impact(conn: sqlite3.Connection, base_sha: str | None) -> str:
    """Blast-radius BFS report.

    Without ``base_sha`` we emit a placeholder. With ``base_sha``, every file
    whose stored sha differs gets a section listing its 1-hop and 2-hop
    outgoing dependents.
    """
    lines = ["# Impact", ""]
    if base_sha is None:
        lines.append("No base SHA provided — impact map unavailable.")
        return "\n".join(lines)

    changed = _changed_paths_for_base(conn, base_sha)
    if not changed:
        lines.append("_No files differ from base SHA._")
        return "\n".join(lines)

    for path in changed:
        lines.append(f"## {path}")
        lines.append("")
        by_depth = _bfs_impact(conn, path, _IMPACT_MAX_DEPTH)
        if not by_depth:
            lines.append("_No outgoing dependents._")
            lines.append("")
            continue
        for depth in sorted(by_depth):
            label = (
                "Direct dependents (1-hop)" if depth == 1 else f"{depth}-hop dependents"
            )
            lines.append(f"### {label}")
            lines.append("")
            for dep in by_depth[depth]:
                lines.append(f"- {dep}")
            lines.append("")
    return "\n".join(lines)


