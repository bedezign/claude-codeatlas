# Signature: (project_root, since) -> list of project-relative file paths.
GitLogFn = Callable[[Path, str], list[str]]


def _default_git_log(project_root: Path, since: str) -> list[str]:
    """Default ``git_log_fn``: shell out to ``git log --name-only --since=...``.

    Returns project-relative paths. Returns an empty list if git is missing
    or the command fails — callers treat absence as "nothing changed".
    """
    try:
        proc = subprocess.run(
            [
                "git",
                "log",
                "--name-only",
                f"--since={since}",
                "--pretty=format:",
                "--",
                ".",
            ],
            cwd=str(project_root),
            capture_output=True,
            text=True,
            check=False,
            timeout=30,
        )
    except subprocess.TimeoutExpired:
        return []
    except FileNotFoundError:
        return []
    if proc.returncode != 0:
        return []
    seen: set[str] = set()
    out: list[str] = []
    for line in proc.stdout.splitlines():
        line = line.strip()
        if not line or line in seen:
            continue
        seen.add(line)
        out.append(line)
    return out


def _file_paths_in_db(conn: sqlite3.Connection) -> set[str]:
    rows = conn.execute("SELECT path FROM files").fetchall()
    return {row[0] for row in rows}


def _render_recent_changes(
    conn: sqlite3.Connection,
    project_root: Path,
    since: str | None,
    git_log_fn: GitLogFn,
) -> str:
    """Recently-changed files (per ``git log --since=...``) + 1-hop dependents."""
    lines = ["# Recent Changes", ""]
    if since is None:
        lines.append("No --since provided — recent-changes map unavailable.")
        return "\n".join(lines)

    git_paths = git_log_fn(project_root, since)
    tracked = _file_paths_in_db(conn)
    relevant = [p for p in git_paths if p in tracked]

    if not relevant:
        lines.append("_No tracked files changed in the requested window._")
        return "\n".join(lines)

    adjacency = _outgoing_neighbours(conn, set(relevant))
    for path in sorted(relevant):
        lines.append(f"## {path}")
        lines.append("")
        deps = sorted(adjacency.get(path, set()))
        if not deps:
            lines.append("_No outgoing dependents._")
            lines.append("")
            continue
        lines.append("Direct dependents (1-hop):")
        lines.append("")
        for dep in deps:
            lines.append(f"- {dep}")
        lines.append("")
    return "\n".join(lines)


