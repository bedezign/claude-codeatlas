def _render_architecture(conn: sqlite3.Connection) -> str:
    """Module-level dependency graph: file clusters by directory."""
    rows = conn.execute("SELECT path FROM files ORDER BY path").fetchall()
    clusters: dict[str, list[str]] = {}
    for (path,) in rows:
        module = _top_module(path) or "(root)"
        clusters.setdefault(module, []).append(path)

    lines = ["# Architecture", ""]
    if not clusters:
        lines.append("_No files indexed._")
        return "\n".join(lines)

    for module in sorted(clusters):
        lines.append(f"## {module}")
        lines.append("")
        for path in sorted(clusters[module]):
            lines.append(f"- {path}")
        lines.append("")
    return "\n".join(lines)


def _render_modules(conn: sqlite3.Connection) -> str:
    """One section per top-level module with its files and symbol counts."""
    rows = conn.execute(
        "SELECT f.path, COUNT(s.id) AS n "
        "FROM files f LEFT JOIN symbols s ON s.file_id = f.id "
        "GROUP BY f.id ORDER BY f.path"
    ).fetchall()

    clusters: dict[str, list[tuple[str, int]]] = {}
    for path, count in rows:
        module = _top_module(path) or "(root)"
        clusters.setdefault(module, []).append((path, count))

    lines = ["# Modules", ""]
    if not clusters:
        lines.append("_No modules indexed._")
        return "\n".join(lines)

    for module in sorted(clusters):
        lines.append(f"## {module}")
        lines.append("")
        lines.append("| File | Symbols |")
        lines.append("| --- | ---: |")
        for path, count in sorted(clusters[module]):
            lines.append(f"| {path} | {count} |")
        lines.append("")
    return "\n".join(lines)


def _render_data(conn: sqlite3.Connection) -> str:
    """Symbols of kind class/attribute/variable with their file and line."""
    placeholders = ",".join("?" * len(_DATA_KINDS))
    rows = conn.execute(
        f"SELECT f.path, s.name, s.kind, s.line "
        f"FROM symbols s JOIN files f ON f.id = s.file_id "
        f"WHERE s.kind IN ({placeholders}) "
        f"ORDER BY f.path, s.line, s.name",
        _DATA_KINDS,
    ).fetchall()

    lines = ["# Data", ""]
    if not rows:
        lines.append("_No data symbols indexed._")
        return "\n".join(lines)

    lines.append(_SYMBOL_TABLE_HEADER)
    lines.append(_SYMBOL_TABLE_DIVIDER)
    for path, name, kind, line in rows:
        line_str = "" if line is None else str(line)
        lines.append(f"| {path} | {name} | {kind} | {line_str} |")
    return "\n".join(lines)


def _render_api(conn: sqlite3.Connection) -> str:
    """Functions called from outside their own file."""
    rows = conn.execute(
        "SELECT DISTINCT fd.path, sd.name, sd.line "
        "FROM edges e "
        "JOIN symbols sd ON sd.id = e.dst_id "
        "JOIN files fd ON fd.id = sd.file_id "
        "JOIN symbols ss ON ss.id = e.src_id "
        "WHERE e.kind = 'calls' AND sd.kind = 'function' "
        "AND ss.file_id <> sd.file_id "
        "ORDER BY fd.path, sd.line, sd.name"
    ).fetchall()

    lines = ["# API", ""]
    if not rows:
        lines.append("_No externally-called functions found._")
        return "\n".join(lines)

    lines.append("Functions invoked from a file other than the one declaring them.")
    lines.append("")
    lines.append("| File | Function | Line |")
    lines.append("| --- | --- | ---: |")
    for path, name, line in rows:
        line_str = "" if line is None else str(line)
        lines.append(f"| {path} | {name} | {line_str} |")
    return "\n".join(lines)


